package org.sersoft.model;

public class DateAlgoritm {

    private float a;
    private float b;
    private float alfa;

    public DateAlgoritm() {

    }

    public DateAlgoritm(float a, float b, float alfa) {
        this.a = a;
        this.b = b;
        this.alfa = alfa;
    }

    public float getA() {
        return a;
    }

    public void setA(float a) {
        this.a = a;
    }

    public float getB() {
        return b;
    }

    public void setB(float b) {
        this.b = b;
    }

    public float getAlfa() {
        return alfa;
    }

    public void setAlfa(float alfa) {
        this.alfa = alfa;
    }
}
